class Shelter
  def initialize
    @adoptions = {}
    @pets = []
  end

  def adopt(owner, pet)
    owner.add_pet(pet)
    @adoptions[owner.name] ||= owner
  end

  def print_adoptions
    @adoptions.each do |name, owner|
      puts "#{name} has adopted the following pets:"
      owner.print_pets
      puts
    end
  end

  def add_pet(pet)
    @pets << pet
  end

  def print_unadopted
    puts "The shelter has the following unadopted pets:"
    puts @pets
  end
end

class Owner
  attr_reader :name, :pets

  def initialize(name)
    @name = name
    @pets = []
  end

  def add_pet(pet)
    @pets << pet
  end

  def number_of_pets
    pets.size
  end

  def print_pets
    puts pets
  end
end

class Pet
  attr_reader :type, :name

  def initialize(type, name)
    @type = type
    @name = name
  end

  def to_s
    "a #{type} named #{name}"
  end
end

